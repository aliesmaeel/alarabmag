<?php

namespace App\Support;

class ExpandedContent
{
    /** @return list<string> */
    public static function paragraphs(string $topic, string $context = ''): array
    {
        $ctx = filled($context) ? " {$context}" : '';

        return [
            "<p>في مشهد {$topic} العربي المعاصر، تتجلّى قصة تُجسّد طموحاً لا يعرف حدوداً.{$ctx} فريق مجلة العرب زار الميدان وتحدث إلى المعنيين مباشرة، ليقدّم تقريراً موسعاً يتجاوز العناوين السريعة ويغوص في التفاصيل التي تُشكّل الفرق.</p>",
            "<p>البدايات لم تكن سهلة. ورغم التحديات الهيكلية والاقتصادية التي تواجه المنطقة، استطاع أبطال هذه القصة تحويل العقبات إلى فرص. خبراء يرون أن هذا النمو ليس صدفة، بل نتيجة سنوات من العمل الصامت والاستثمار في المعرفة والمواهب المحلية.</p>",
            "<p>من منظور اقتصادي، يُظهر التقرير أن {$topic} أصبح محركاً حقيقياً للتغيير. البيانات الأخيرة تشير إلى نمو ملحوظ في الاهتمام الإقليمي والدولي، مع تزايد الطلب على المحتوى العربي الأصيل الذي يقدّم رؤية متوازنة من داخل المنطقة.</p>",
            "<p>«نحن لا نبني مشروعاً مؤقتاً، بل نؤسّس لمسار طويل الأمد»، يقول أحد المشاركين في الحوار. هذه العبارة تلخّص فلسفة كثير من رواد هذا المجال، الذين يربطون النجاح بالصبر والاستدامة والالتزام بالقيم.</p>",
            "<p>على صعيد المجتمع، يترك {$topic} أثراً واضحاً. الشباب العربي يرى في هذه النماذج مصدر إلهام، بينما المؤسسات التعليمية والثقافية تتكيّف مع متطلبات جديدة. التحول الرقمي لعب دوراً محورياً في تسريع هذا التأثير عبر الحدود.</p>",
            "<p>لا يمكن فهم الصورة كاملة دون النظر إلى السياق الإقليمي. من الخليج إلى المشرق وشمال أفريقيا، تتنوع التجارب لكنها تتقاطع حول هدف مشترك: إثبات أن الإنسان العربي قادر على المنافسة العالمية دون التخلي عن هويته.</p>",
            "<p>محللون يرون أن المرحلة القادمة ستحمل مزيداً من التحديات، لكن أيضاً فرصاً غير مسبوقة. الاستثمار في البحث والتطوير، إلى جانب الشراكات الدولية الذكية، سيكونان مفتاح النجاح على المدى الطويل.</p>",
            "<p>في ختام تحقيقنا، يتّضح أن {$topic} ليس مجرد عنوان إخباري عابر، بل جزء من تحول أوسع في الوعي العربي المعاصر. مجلة العرب ستتابع هذه القصة وتحرص على تقديم متابعة دورية للقارئ العربي المهتم بالعمق لا بالسطحية.</p>",
        ];
    }

    public static function articleBody(string $title, string $excerpt, ?string $existing = null): string
    {
        $topic = mb_substr(strip_tags($title), 0, 80);
        $intro = filled($existing) ? $existing : '<p>' . e(strip_tags($excerpt)) . '</p>';
        $paragraphs = array_merge([$intro], static::paragraphs($topic, $excerpt));

        return implode("\n", $paragraphs);
    }

    public static function bioBody(string $name, string $role, string $excerpt, ?string $existing = null): string
    {
        $intro = filled($existing) ? $existing : '<p>' . e(strip_tags($excerpt)) . '</p>';

        return implode("\n", [
            $intro,
            "<p>وُلد {$name} ونشأ في بيئة غذّت شغفه بـ{$role}. منذ سنوات مبكرة، أظهر موهبة استثنائية جعلته محط أنظار المختصين في مجاله. رحلته لم تكن مفروشة بالورود، لكن الإصرار والعمل الدؤوب كانا رفيقيه في كل محطة.</p>",
            "<p>على مدى العقدين الماضيين، حقق {$name} سلسلة من الإنجازات التي وضعته في صدارة {$role} على المستوى العربي. زملاؤه يصفونه بالقائد الهادئ الذي يجمع بين الرؤية الاستراتيجية والاهتمام بالتفاصيل. «التميّز عادة وليس حدثاً»، يكرّر في كل لقاء.</p>",
            "<p>خارج نطاق عمله المباشر، يشارك {$name} في مبادرات مجتمعية تهدف إلى تمكين الجيل القادم. يرى أن على كل شخصية عربية ناجحة أن تُعيد جزءاً مما حصل عليه إلى مجتمعه، سواء عبر التعليم أو الإرشاد أو الدعم المؤسسي.</p>",
            "<p>في مقابلة حصرية مع مجلة العرب، تحدث {$name} عن التحديات التي واجهها والدروس المستفادة. أكد أن النجاح الحقيقي يُقاس بالأثر لا بالأرقام وحدها، وأن الحفاظ على الهوية العربية جزء لا يتجزأ من أي مشروع طموح.</p>",
            "<p>نظراً إلى مساره وإنجازاته، يُعد {$name} من أبرز الشخصيات التي تستحق المتابعة في {$role}. مجلة العرب تفتخر بتقديم هذا الملف الشامل، وستواصل متابعة مسيرته في أعدادها القادمة.</p>",
        ]);
    }
}
